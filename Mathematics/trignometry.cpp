class trignometry {
    public:
        double sinusoidal (double base, double tangent)
        {
            double sinusoidal = base/tangent;
            return sinusoidal;
        }
        
        double cosinusoidal (double height, double tangent)
        {
            double cosinusoidal = height/tangent;
            return cosinusoidal;
        }
        
        double tangent (double height, double base)
        {
            double tangent = height/base;
            return tangent;
        }
};
